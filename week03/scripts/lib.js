// export will export the functions so they can be import'd elsewhere

/**
 * calculates the half of a number
 * @param {*} x
 * @returns
 */
export function halfOf(x = 40) {
  return x / 2;
}

/**
 * multiple 2 numbers
 * @param {*} x
 * @param {*} y
 * @returns
 */
export function multiply(x, y) {
  return x * y;
}

